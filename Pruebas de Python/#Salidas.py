print (cadena)

cadena = "hola mundo".find("o") #Busca el indice de la letra o palabaras

print (cadena)

cadena = "hola mundo".rfind("o") #Busca la ultima coincidencia de la letra o palabaras

print (cadena)

cadena = "1000".isdigit() #Devuelve TRUE si solo tiene numeros

