#Entrada de datos

#Solo funciona con cadenas de caracteres, no guarda los numeros como variables
Nombre = input ("Ingrese su nombre: \n")

print (f"Hola {Nombre}, como estás?")
#Variable de tipo entero, también funciona con float
Nombre = int (input ("Ingrese su nombre: \n"))

print (f"Hola {Nombre+1}, como estás?")